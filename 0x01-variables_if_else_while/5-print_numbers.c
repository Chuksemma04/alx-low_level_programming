#include <stdio.h>


/**
 *
 * main - Write a program that prints all single digit numbers of base 10
 *
 * Description: prints Uppercase and Lowercase Alphabets
 *
 * Return: Always 0 (Success)
 *
 */

int main(void)

{

	int i;



	for (i = 0; i < 10; i++)

	{

	printf("%d", i);

	}

	putchar('\n');

	return (0);

}
